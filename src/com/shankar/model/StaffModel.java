package com.shankar.model;

public class StaffModel {
	private int id;
	private String name;
	private String fatherName;
	private String motherName;
	private String contactNo;
	private String qualification;
	private String email;
	private String designation;
	private String languagesKnown;
	private String subjectsThought;
	private String classAlloted;
	private String sectionAlloted;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFatherName() {
		return fatherName;
	}
	public void setFatherName(String fatherName) {
		this.fatherName = fatherName;
	}
	public String getMotherName() {
		return motherName;
	}
	public void setMotherName(String motherName) {
		this.motherName = motherName;
	}
	public String getContactNo() {
		return contactNo;
	}
	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getLanguagesKnown() {
		return languagesKnown;
	}
	public void setLanguagesKnown(String languagesKnown) {
		this.languagesKnown = languagesKnown;
	}
	public String getSubjectsThought() {
		return subjectsThought;
	}
	public void setSubjectsThought(String subjectsThought) {
		this.subjectsThought = subjectsThought;
	}
	public String getClassAlloted() {
		return classAlloted;
	}
	public void setClassAlloted(String classAlloted) {
		this.classAlloted = classAlloted;
	}
	public String getSectionAlloted() {
		return sectionAlloted;
	}
	public void setSectionAlloted(String sectionAlloted) {
		this.sectionAlloted = sectionAlloted;
	}
	

}